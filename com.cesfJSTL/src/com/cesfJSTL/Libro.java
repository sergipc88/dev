package com.cesfJSTL;

public class Libro {

	private String titulo;
	private int precio;
	
	public Libro(String titulo, int precio) {
		super();
		this.titulo = titulo;
		this.precio = precio;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public int getPrecio() {
		return precio;
	}

	public void setPrecio(int precio) {
		this.precio = precio;
	}
	
	
	
	
	
}
